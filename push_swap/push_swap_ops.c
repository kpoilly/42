#include "push_swap.h"

void	sa(t_stack **a)
{
	*a = swap(a);
}

void	sb(t_stack **b)
{
	*b = swap(b);
}

void	ss(t_stack **a, t_stack **b)
{
	sa(a);
	sb(b);
}
