#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	if (i == ft_strlen(s) && s[i] != c)
		return (NULL);
	return ((char *)s + i);
}
