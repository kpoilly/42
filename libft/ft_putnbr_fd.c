#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	nb;
	int			sign;

	nb = n;
	sign = 1;
	if (nb < 0)
	{
		sign *= -1;
		nb *= -1;
		ft_putchar_fd('-', fd);
	}
	if (nb > 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
		ft_putchar_fd((nb + '0'), fd);
}
