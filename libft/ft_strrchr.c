#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	if (!s)
		return (NULL);
	i = ft_strlen(s);
	while (i >= 0 && s[i] != c)
		i--;
	if (i == -1)
		return (NULL);
	return ((char *)s + i);
}
