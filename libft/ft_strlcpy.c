#include "libft.h"

unsigned int	ft_strlcpy(char *dest, const char *src, unsigned int size)
{
	unsigned int	longlen;
	unsigned int	i;

	longlen = ft_strlen(src);
	i = 0;
	if (size <= 0)
		return (longlen);
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (longlen);
}
